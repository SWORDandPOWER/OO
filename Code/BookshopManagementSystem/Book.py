class Book(object):

    def __init__(self):
        self.bookname = ""
        self.author = ""
        self.inventory = 0
        self.salenum = 0
        self.bid = 0
        self.price = 0

    def get_bookname(self):
        return self.bookname

    def get_author(self):
        return self.author

    def get_inventory(self):
        return self.inventory

    def get_salenum(self):
        return self.salenum

    def get_bid(self):
        return self.bid

    def get_price(self):
        return self.price

    def set_bookname(self):
        return self.bookname

    def set_author(self):
        return self.author

    def set_inventory(self):
        return self.inventory

    def set_salenum(self):
        return self.salenum

    def set_bid(self):
        return self.bid

    def set_price(self):
        return self.price
